    private void btnOkayRegVentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkayRegVentActionPerformed
        //________________BOTON OKAY!!!!_________________
        //empiezo declarando sin crearlo porque no se puede guardar como Articulo, sino como clases hijas
        Articulo art1;
        
            
        
        
        
        //verifico que no exista ese codigo
     if(listaP.isOcupado(Integer.parseInt(fieldCodigo.getText()))==false){
         
         // Transformo el objeto articulo a alguna de sus 2 clases hijas
        // segun que' radio button esta seleccionado:
        if(radBtnNacional.isSelected()){
            art1=new Nacional();
             setDatos(art1); 
        }else{
            if(radBtnImportado.isSelected()){
                art1=new Importado();
                 setDatos(art1); 
            }
            if(radBtnImportado.isSelected()==false && radBtnNacional.isSelected()==false){
                this.setTitle("Seleccione Importado o Nacional");
            }
                
        }
     }else{
         JOptionPane.showMessageDialog(null,"Codigo Ocupado");
         fieldCodigo.setText(null);
     }
    }//GEN-LAST:event_btnOkayRegVentActionPerformed

