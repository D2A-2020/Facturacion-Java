    public void setDatos(Articulo aux){
        if(aux instanceof Nacional){
            ((Nacional) aux).setSubsidiado(checkBoxSubsidiado.isSelected());
            ((Nacional) aux).setDepartamento(fieldDepto.getText());
         }
        if(aux instanceof Importado){
            ((Importado) aux).setImpuestoAduana(Double.parseDouble(fieldImpuesto.getText()));
            ((Importado) aux).setAnoImportacion(Integer.parseInt(fieldAnoImp.getText()));
        }
        
       
        aux.setCodigo(Integer.parseInt(fieldCodigo.getText()));
        aux.setNombre(fieldNombre.getText());
        aux.setPrecioCosto(Double.parseDouble(fieldPrecioCosto.getText()));
        aux.setUnidades(Integer.parseInt(fieldUnidades.getText()));   
        System.out.println(aux);//prueba de que funciona
        listaP.insertar(aux);
        
        
    } 
    
